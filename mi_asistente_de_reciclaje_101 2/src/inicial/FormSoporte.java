    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        String modulo=combo_modulo.getSelectedItem().toString();
        String otro=txt_modulo.getText();
            if(modulo.equalsIgnoreCase("Seleccionar")){
                
                 JOptionPane.showMessageDialog(null, "Debe de seleccionar un modulo");
                 
             }else{
                
                if(otro.equalsIgnoreCase("Otro")){
                    
                    JOptionPane.showMessageDialog(null, "Especifique el m odulo en el campo de texto");
                    txt_modulo.setVisible(true);
                    
                }else{
                    
                    combo_modulo.setVisible(false);
                    txt_modulo.setVisible(false);
                    CQuejas objetoQuejas = new CQuejas();
                    objetoQuejas.RegistroQuejas(txt_modulo);
                    FormQuejaGuardad objetoGuardar = new FormQuejaGuardad();
                    objetoGuardar.setVisible(true);
                    this.dispose();
            }
            }
        
    }//GEN-LAST:event_jButton2ActionPerformed

